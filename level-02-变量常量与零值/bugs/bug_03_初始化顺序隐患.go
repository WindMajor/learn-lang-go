// WHAT: Bug 03 — 全局变量初始化顺序陷阱
// ERROR: 依赖全局变量的初始化顺序是一种隐患
//        Go 保证同一包内按声明顺序初始化，但跨包的 init 顺序由导入依赖决定
//        当 init 函数之间相互依赖时，问题就来了
//
// ============================================================
// 运行结果：
// init 执行，dbSize = 100
// main 中 dbSize = 100
// ============================================================
//
// 但如果我们有另一个文件 init_other.go 在同一包中也定义了 init
// 并且修改了 dbSize，两个 init 的执行顺序由文件名决定（按字母序）——
// 这是 Go 规范允许但极其脆弱的！依赖文件名排序就是个定时炸弹。
//
// CONTRAST（与已知语言对比）：
//   - Rust: 无 init 机制，依赖 lazy_static/once_cell 显式控制初始化
//   - C:    全局初始化顺序由链接器决定（也可能不确定）
//   - Go:   同包内按声明顺序 → 确定但不直观
//          同文件多个 init 按出现顺序 → 确定
//          不同文件 init 按文件名排序 → 确定但脆弱！
//          跨包 init 由依赖图决定 → 确定但复杂
//
// 如何避免：
//   1. init 中只做简单的初始化（注册、设置默认值）
//   2. 不要在 init 中依赖其他包的 init 的执行结果
//   3. 如果初始化逻辑复杂，用显式的 Init() 函数而不是 init
//
// ============================================================

package main

import "fmt"

// 全局变量在 init 之前初始化
var dbSize = 0

// init 函数在全局变量初始化之后、main 之前执行
func init() {
	fmt.Println("init 执行，设置 dbSize")
	dbSize = 100 // 修改全局变量
}

// 如果存在另一个 init（在另一个文件中），
// 它们会按文件名排序执行，这非常脆弱！
// 试想：如果 bug_02_iota.go 中有个 init 修改了 dbSize = 200
//       那结果取决于文件名排序！

func main() {
	fmt.Printf("main 中 dbSize = %d\n", dbSize)
	// 输出：100
	// 但如果同包其他文件的 init 也修改了它，值就不确定了！

	fmt.Println("\n注意：如果同包有多个 init 函数，执行顺序按文件名排序。")
	fmt.Println("这个行为是确定的，但很脆弱 —— 重命名文件就可能改变顺序！")
}
